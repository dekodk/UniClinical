package entidade;


public class Export {
    private String clienteExport;
    private String procedimentoExport;
    private String insumoExport;
    private String valorExport;

    public String getClienteExport() {
        return clienteExport;
    }

    public void setClienteExport(String clienteExport) {
        this.clienteExport = clienteExport;
    }

    public String getProcedimentoExport() {
        return procedimentoExport;
    }

    public void setProcedimentoExport(String procedimentoExport) {
        this.procedimentoExport = procedimentoExport;
    }

    public String getInsumoExport() {
        return insumoExport;
    }

    public void setInsumoExport(String insumoExport) {
        this.insumoExport = insumoExport;
    }

    public String getValorExport() {
        return valorExport;
    }

    public void setValorExport(String valorExport) {
        this.valorExport = valorExport;
    }
    
    
}
