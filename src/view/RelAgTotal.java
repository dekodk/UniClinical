public void buscar(String dataInicialFormatada, String dataFinalFormatada) {
        // Defina as colunas da tabela
        String[] colunas = {"Cód.Ag.", "Cliente", "Data", "Valor"};

        // Crie o modelo da tabela com as colunas
        DefaultTableModel modelo = new DefaultTableModel(null, colunas);
        Connection conexao = FabricaConexao.getConnection();

        try {
            // Prepare a query para buscar o orçamento com o ID e ativo como 'SIM'
            PreparedStatement pstm = conexao.prepareStatement("SELECT * FROM agendamento WHERE dataAgendamento BETWEEN ? AND ? ORDER BY dataAgendamento ASC ");
            pstm.setString(1, dataInicialFormatada);
            pstm.setString(2, dataFinalFormatada);

            // Execute a consulta
            ResultSet rs = pstm.executeQuery();

            // Preencha o modelo com os dados da consulta
            while (rs.next()) {
                String[] dados = {
                    String.valueOf(rs.getInt("idAgendamento")),
                    rs.getString("nomeCliente"),
                    DataFormat.formatarData(rs.getDate("dataAgendamento")),
                    "R$ " + rs.getString("valorTotal")
                };
                modelo.addRow(dados);  // Adiciona uma nova linha ao modelo
            }

            // Defina o modelo para a JTable
            jTable1.setModel(modelo);

        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            try {
                if (conexao != null) {
                    conexao.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
