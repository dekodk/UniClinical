    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        // TODO add your handling code here:

        Date dataInicial = jDataInicial.getDate();
        Date dataFinal = jDataFinal.getDate();

        if (dataInicial != null && dataFinal != null) {
            String dataInicialFormatada = DataFormat.formatarDataParaSql(dataInicial); // yyyy-MM-dd
            String dataFinalFormatada = DataFormat.formatarDataParaSql(dataFinal);

            buscar(dataInicialFormatada, dataFinalFormatada);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione as duas datas para realizar a busca.");
        }

    }//GEN-LAST:event_jButtonPesquisarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Date dataInicial = jDataInicial.getDate();
        Date dataFinal = jDataFinal.getDate();
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        int totalLinhas = modelo.getRowCount();

        if (totalLinhas == 0) {
            JOptionPane.showMessageDialog(null, "A tabela está vazia. Faça uma busca antes.");
            return;
        }

        StringBuilder ids = new StringBuilder();

        for (int i = 0; i < totalLinhas; i++) {
            String id = modelo.getValueAt(i, 0).toString().trim();
            ids.append(id);
            if (i < totalLinhas - 1) {
                ids.append(",");
            }
        }

        Map<String, Object> parametros = new HashMap<>();
        parametros.put("param_data_inicial", dataInicial);
        parametros.put("param_data_final", dataFinal);

        int ok = JOptionPane.showConfirmDialog(null, "Confirma a impressão do relatório com todos os agendamentos listados?", "Impressão", JOptionPane.YES_NO_OPTION);
        if (ok == JOptionPane.YES_OPTION) {
            try {
                JasperPrint print = JasperFillManager.fillReport("C:\\UniClinical\\Relatorios\\AgendamentosData.jasper", parametros, conexao);
                JasperViewer.viewReport(print, false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

